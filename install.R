# To download project from RStudio Cloud
#
# 1. Go one step above the "project" folder (into the "cloud" folder)
# 2. Click "project" folder and click "More" >> "Export"
# 3. save it onto your machine and unzip
# 4. Open the "project.rproj" file to open in RStudio Desktop
# 5. Install the necessary packages using the command below:

install.packages(c("tidyverse", "tidyquant", "quantmod", "distill"))
